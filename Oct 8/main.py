num1 = int(input("Enter number 1: "))
num2 = int(input("Enter number 2: "))
num3 = int(input("Enter number 3: "))

print("The sum of num1 and num2 is: ",num1 + num2)                                  #Addition

print("The difference when num2 is subtracted from num1 is: ",num1 - num2)          #Subtraction

print("The product of num1 and num2 is: ",num1 * num2)                              #Multiplication

print("The quotient when num1 is divided by num2 is: ",num1 / num2)                 #Division

print("The remainder when num1 is divided by num2 is: ", num1 % num2)               #Remainder

print("The result of (num1 + num2) * num3 / 2 is: ",(num1 + num2) * num3 / 2)       #Combined Operation









