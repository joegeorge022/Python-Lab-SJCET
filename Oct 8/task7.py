str1 = "Joe"                       #The first string containing first name.
str2 = "George"                    #The second string containing last name.

str3 =str1+" "+str2                #Concatenated the two strings & store result in a new variable.

print(str3)                        #Prints the concatenated string.

str4 = str3 [4:]                   #Uses string slicing to extract the sub-string.

print(str4)                        #Printed the sub-string.










