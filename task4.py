# math module is imported to perform certain mathematical functions.
import math

#The below line asks the user for a number input.
x = int(input("Enter a number: "))

#Below line calculates the square root of the input.
print("Square root of ",x,":",math.sqrt(x))

#Below line calculates the factorial of the input.
print("Factorial of",x,":",math.factorial(x))

#Below line calculates the 2nd power of the input.
print(x,"raised to power 2: ",math.pow(x,2))













